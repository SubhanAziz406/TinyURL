        <h2 class="text-center mb-4">Security Policy</h2>


        <p>At TheTinyURLs.com, we take the security of our users and their data seriously. This Security Policy outlines the steps we take to protect your information and ensure the integrity of our services. By using TheTinyURLs.com, you agree to the terms outlined in this policy.</p>
        <div class="privacy-wrapper">

            <!-- Section 1: Data Protection Measures -->
           
                <h5> Data Protection Measures</h5>
                <p>We implement industry-standard security measures to protect the information and data shared on TheTinyURLs.com from unauthorized access, disclosure, alteration, or destruction. Our security measures include:</p>
                <ul>
                    <li><strong>Encryption:</strong> All sensitive data, including passwords and personal information, is encrypted using industry-standard encryption protocols during transmission and storage.</li>
                    <li><strong>Secure Connections (SSL/TLS):</strong> We use Secure Sockets Layer (SSL) or Transport Layer Security (TLS) to encrypt data transmissions between your browser and our servers, ensuring that your data is protected during transit.</li>
                    <li><strong>Access Controls:</strong> Access to user data is restricted to authorized personnel only. Employees, contractors, and partners with access to sensitive data undergo strict access controls and security training.</li>
                </ul>
         

            <!-- Section 2: User Responsibilities -->
         
                <h5> User Responsibilities</h5>
                <p>While we take all reasonable steps to protect your data, users are responsible for:</p>
                <ul>
                    <li><strong>Account Security:</strong> Protecting your account credentials (username and password) and not sharing them with others. We recommend using strong, unique passwords for your account.</li>
                    <li><strong>Phishing Awareness:</strong> Being cautious of phishing scams and only providing personal information through our official website or app.</li>
                    <li><strong>Software Updates:</strong> Keeping your devices, operating systems, and software up to date with the latest security patches to prevent vulnerabilities.</li>
                </ul>
         

            <!-- Section 3: Vulnerability Management -->
    
                <h5> Vulnerability Management</h5>
                <p>We employ a proactive approach to identifying and addressing vulnerabilities in our system:</p>
                <ul>
                    <li><strong>Regular Security Audits:</strong> We conduct regular security audits and assessments to detect and fix vulnerabilities in our infrastructure.</li>
                    <li><strong>Third-Party Security Tools:</strong> We use trusted third-party security tools to scan and monitor for any potential threats or vulnerabilities.</li>
                    <li><strong>Patch Management:</strong> Software updates and patches are promptly applied to fix any known security issues.</li>
          </ul>

            <!-- Section 4: Incident Response -->
    
                <h5> Incident Response</h5>
                <p>In the event of a security breach or incident:</p>
                <ul>
                    <li><strong>Monitoring:</strong> Our systems are continuously monitored for suspicious activities, unauthorized access attempts, and security breaches.</li>
                    <li><strong>Notification:</strong> If a data breach occurs, we will notify affected users promptly via email, outlining the nature of the breach, the data affected, and any recommended steps to mitigate potential harm.</li>
                    <li><strong>Resolution:</strong> We will take immediate action to contain the breach, investigate the cause, and restore the security and functionality of our services.</li>
                </ul>
          

            <!-- Section 5: Data Retention and Disposal -->
            
                <h5> Data Retention and Disposal</h5>
                <p>We retain user data only for as long as necessary to fulfill the purposes for which it was collected, or as required by law. When data is no longer needed, we securely delete or anonymize it.</p>
                <ul>
                    <li><strong>Secure Deletion:</strong> User data is securely deleted from our servers following strict data disposal procedures, ensuring that it cannot be recovered or misused.</li>
                    <li><strong>Data Minimization:</strong> We strive to minimize the amount of personal information we collect and store to reduce potential security risks.</li>
                </ul>
            

            <!-- Section 6: Third-Party Services -->
           
                <h5> Third-Party Services</h5>
                <p>We work with trusted third-party service providers to deliver some aspects of our services. These providers are subject to rigorous security standards, and we ensure that they follow industry best practices for data protection.</p>
                <ul>
                    <li><strong>Data Sharing:</strong> We only share necessary information with third-party providers, and they are contractually obligated to protect your data.</li>
                    <li><strong>Due Diligence:</strong> We perform due diligence on all third-party providers to assess their security practices before integrating their services.</li>
                </ul>
            

            <!-- Section 7: Reporting Security Issues -->
          
